        private void descargarPdf(byte[] pdfBytes) {
        JFileChooser fc = new JFileChooser();
        if (fc.showSaveDialog(this) == JFileChooser.APPROVE_OPTION) {
            try (FileOutputStream fos = new FileOutputStream(fc.getSelectedFile())) {
                fos.write(pdfBytes);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Error al guardar PDF: " + ex.getMessage());
            }
        }
    }

    private void imprimirPdf(byte[] pdfBytes) {
        try {
            PDDocument doc = PDDocument.load(pdfBytes);
            PrinterJob job = PrinterJob.getPrinterJob();
            job.setPageable(new PDFPageable(doc));
            if (job.printDialog()) {
                job.print();
            }
            doc.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al imprimir PDF: " + ex.getMessage());
        }
    }


